#Afficher l'ensemble des éléments de la liste grâce à une boucle
liste = [1, 15, -3, 0, 8, 7, 4, -2, 28, 7, -1, 17, 2, 3, 0, 14, -4]
for elm in liste:
    print(elm)
print("-------------------------------")

#Afficher l'ensemble des éléments de la liste dans l'ordre inverse
for inv_elm in reversed(liste):
    print(inv_elm)
